#include <stdio.h>

int main(void)
{
    int x =10;
    int y =30;

    printf("x는 %d입니다.\n", x);
    printf("y는 %d입니다.\n", y);
    printf("x+y는 %d입니다.\n", x+y);
    printf("x-y는 %d입니다.\n", x-y);
    printf("x*y는 %d입니다.\n", x*y);
    printf("x/y는 %d입니다.\n", x/y);
    printf("x%y는 %d입니다.\n", x%y);

    return 0;
}
